# Generate 20 unique course entries for each department
courses = []
for dept_id in departments:
    for i in range(1, 21):
        course_name = course_data[dept_id]["names"][i-1]
        description = course_data[dept_id]["descriptions"][i-1]
        credits = random.choice([2, 3, 4])
        teacher_id = random.randint(1, 200)
        start_date = datetime(2024, 9, 1) + timedelta(days=random.randint(0, 30))
        end_date = start_date + timedelta(days=random.randint(90, 180))

        courses.append([
            len(courses) + 1, course_name, description, credits, teacher_id, dept_id,
            start_date.strftime("%Y-%m-%d"), end_date.strftime("%Y-%m-%d")
        ])

# Writing to CSV file
with open('courses.csv', mode='w', newline='', encoding='utf-8') as file:
    writer = csv.writer(file)
    writer.writerow(['course_id', 'course_name', 'description', 'credits', 'teacher_id', 'department_id', 'start_date', 'end_date'])
    writer.writerows(courses)

print("Course data has been written to courses.csv")
